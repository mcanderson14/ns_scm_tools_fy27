import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import vm from "node:vm";

const PROJECT_DIR = dirname(fileURLToPath(import.meta.url));
const DEFAULT_REQUEST_FILE = join(PROJECT_DIR, "codex-calendar-sync-request.json");
const DEFAULT_STATE_FILE = join(PROJECT_DIR, "codex-calendar-sync-state.json");

function normalizeEmail(value) {
  return String(value || "").trim().toLowerCase();
}

function readJson(file) {
  return JSON.parse(readFileSync(file, "utf8"));
}

function writeJson(file, data) {
  writeFileSync(file, `${JSON.stringify(data, null, 2)}\n`);
}

function readDirectConnectorFile(file) {
  const context = { window: {} };
  vm.createContext(context);
  vm.runInContext(readFileSync(file, "utf8"), context);
  return {
    roster: Array.isArray(context.window.SC_STAFFING_IMPORTED_ROSTER) ? context.window.SC_STAFFING_IMPORTED_ROSTER : [],
    loadedEmails: Array.isArray(context.window.DIRECT_CONNECTOR_LOADED_EMAILS) ? context.window.DIRECT_CONNECTOR_LOADED_EMAILS : [],
    availability: Array.isArray(context.window.DIRECT_CONNECTOR_AVAILABILITY) ? context.window.DIRECT_CONNECTOR_AVAILABILITY : [],
    events: Array.isArray(context.window.DIRECT_CONNECTOR_EVENTS) ? context.window.DIRECT_CONNECTOR_EVENTS : [],
    warnings: Array.isArray(context.window.DIRECT_CONNECTOR_SYNC_WARNINGS) ? context.window.DIRECT_CONNECTOR_SYNC_WARNINGS : [],
    metadata: context.window.DIRECT_CONNECTOR_SYNC_METADATA || null
  };
}

function buildDirectConnectorFile(data, refreshedAt = new Date().toISOString()) {
  return [
    "// Generated by Codex Outlook Calendar connector sync.",
    `// Refreshed at: ${refreshedAt}`,
    `window.SC_STAFFING_IMPORTED_ROSTER = ${JSON.stringify(data.roster || [], null, 2)};`,
    "",
    `window.DIRECT_CONNECTOR_LOADED_EMAILS = ${JSON.stringify(data.loadedEmails || [], null, 2)};`,
    "",
    `window.DIRECT_CONNECTOR_AVAILABILITY = ${JSON.stringify(data.availability || [], null, 2)};`,
    "",
    `window.DIRECT_CONNECTOR_EVENTS = ${JSON.stringify(data.events || [], null, 2)};`,
    "",
    `window.DIRECT_CONNECTOR_SYNC_WARNINGS = ${JSON.stringify(data.warnings || [], null, 2)};`,
    "",
    `window.DIRECT_CONNECTOR_SYNC_METADATA = ${JSON.stringify({
      source: "codex-calendar-sync",
      refreshedAt,
      totalConsultants: (data.roster || []).length,
      loadedEmails: (data.loadedEmails || []).length,
      availabilitySnapshots: (data.availability || []).length,
      events: (data.events || []).length,
      warningCount: (data.warnings || []).length
    }, null, 2)};`,
    ""
  ].join("\n");
}

function normalizeResult(payload, fallbackChunk) {
  const data = payload.calendarData || payload;
  const loadedEmails = Array.isArray(data.loadedEmails) && data.loadedEmails.length
    ? data.loadedEmails.map(normalizeEmail).filter(Boolean)
    : [...new Set([
        ...(Array.isArray(data.availability) ? data.availability.map(item => item.email) : []),
        ...(Array.isArray(data.events) ? data.events.map(item => item.email) : []),
        ...(fallbackChunk?.emails || [])
      ].map(normalizeEmail).filter(Boolean))];

  const start = data.start || fallbackChunk?.start || "";
  const intervalMinutes = Number(data.intervalMinutes || fallbackChunk?.intervalMinutes || 30);
  const availability = Array.isArray(data.availability)
    ? data.availability.map(snapshot => ({
        email: normalizeEmail(snapshot.email),
        start: new Date(snapshot.start || `${start}T00:00:00Z`).toISOString(),
        intervalMinutes: Number(snapshot.intervalMinutes || intervalMinutes),
        view: String(snapshot.view || snapshot.availabilityView || snapshot.availability_view || "")
      })).filter(snapshot => snapshot.email && snapshot.view)
    : [];

  const events = Array.isArray(data.events)
    ? data.events.map(event => ({
        email: normalizeEmail(event.email),
        status: String(event.status || "busy"),
        subject: String(event.subject || "Free/busy: Busy; subject unavailable"),
        start: new Date(event.start).toISOString(),
        end: new Date(event.end).toISOString()
      })).filter(event => event.email && event.start && event.end)
    : [];

  return {
    refreshedAt: data.refreshedAt || new Date().toISOString(),
    loadedEmails,
    availability,
    events,
    errors: Array.isArray(data.errors) ? data.errors : [],
    roster: Array.isArray(data.roster) ? data.roster : []
  };
}

function mergeResult({ requestFile, stateFile, resultFile, chunkIndex }) {
  const request = readJson(requestFile);
  const state = readJson(stateFile);
  const result = readJson(resultFile);
  const chunk = request.chunks[chunkIndex];
  if (!chunk) throw new Error(`Chunk ${chunkIndex} was not found in ${requestFile}`);

  const outputFile = state.outputFile || request.output.directConnectorEventsFile || join(dirname(requestFile), "direct-connector-events.js");
  const existing = readDirectConnectorFile(outputFile);
  const normalized = normalizeResult(result, {
    ...chunk,
    start: request.request.start,
    intervalMinutes: request.request.intervalMinutes
  });

  const refreshedEmails = new Set(normalized.loadedEmails);
  const keepEmail = item => !refreshedEmails.has(normalizeEmail(item.email));
  const rosterByEmail = new Map([
    ...(request.request.roster || []).map(person => [normalizeEmail(person.email), { ...person, email: normalizeEmail(person.email) }]),
    ...(existing.roster || []).map(person => [normalizeEmail(person.email), { ...person, email: normalizeEmail(person.email) }]),
    ...(chunk.roster || []).map(person => [normalizeEmail(person.email), { ...person, email: normalizeEmail(person.email) }]),
    ...(normalized.roster || []).map(person => [normalizeEmail(person.email), { ...person, email: normalizeEmail(person.email) }])
  ].filter(([email]) => email));

  const merged = {
    roster: [...rosterByEmail.values()].sort((a, b) => String(a.name || a.email).localeCompare(String(b.name || b.email))),
    loadedEmails: [...new Set([...existing.loadedEmails, ...normalized.loadedEmails].map(normalizeEmail).filter(Boolean))].sort(),
    availability: [
      ...existing.availability.filter(keepEmail),
      ...normalized.availability
    ],
    events: [
      ...existing.events.filter(keepEmail),
      ...normalized.events
    ],
    warnings: [
      ...(existing.warnings || []).filter(item => !refreshedEmails.has(normalizeEmail(item.email))),
      ...(normalized.errors || [])
    ]
  };

  writeFileSync(outputFile, buildDirectConnectorFile(merged, normalized.refreshedAt));

  const completed = new Set(state.completedChunks || []);
  completed.add(chunkIndex);
  const failed = new Set(state.failedChunks || []);
  failed.delete(chunkIndex);
  state.updatedAt = new Date().toISOString();
  state.status = completed.size >= request.chunks.length ? "complete" : "in_progress";
  state.completedChunks = [...completed].sort((a, b) => a - b);
  state.failedChunks = [...failed].sort((a, b) => a - b);
  state.nextChunkIndex = request.chunks.findIndex((_, index) => !completed.has(index));
  if (state.nextChunkIndex < 0) state.nextChunkIndex = request.chunks.length;
  state.lastMerge = {
    chunkIndex,
    loadedEmails: normalized.loadedEmails.length,
    availabilitySnapshots: normalized.availability.length,
    events: normalized.events.length,
    errors: normalized.errors
  };
  writeJson(stateFile, state);

  return {
    outputFile,
    stateFile,
    completedChunks: state.completedChunks.length,
    totalChunks: request.chunks.length,
    loadedEmails: merged.loadedEmails.length,
    availabilitySnapshots: merged.availability.length,
    events: merged.events.length,
    nextChunkIndex: state.nextChunkIndex,
    errors: normalized.errors
  };
}

function showStatus({ requestFile, stateFile }) {
  const request = readJson(requestFile);
  const state = readJson(stateFile);
  const outputFile = state.outputFile || request.output.directConnectorEventsFile;
  const existing = readDirectConnectorFile(outputFile);
  return {
    syncId: request.syncPlan.syncId,
    status: state.status,
    nextChunkIndex: state.nextChunkIndex,
    totalChunks: request.chunks.length,
    totalConsultants: request.syncPlan.totalConsultants,
    completedChunks: state.completedChunks || [],
    failedChunks: state.failedChunks || [],
    outputFile,
    loadedEmails: existing.loadedEmails.length,
    availabilitySnapshots: existing.availability.length,
    events: existing.events.length,
    warnings: existing.warnings.length,
    refreshedAt: existing.metadata?.refreshedAt || null
  };
}

function initState({ requestFile, stateFile, force = false }) {
  const request = readJson(requestFile);
  let existing = null;
  try {
    existing = readJson(stateFile);
  } catch {
    existing = null;
  }

  const syncId = request.syncPlan?.syncId;
  if (!syncId) throw new Error(`Request file does not include syncPlan.syncId: ${requestFile}`);

  if (!force && existing?.syncId === syncId) {
    return {
      stateFile,
      syncId,
      status: existing.status,
      nextChunkIndex: existing.nextChunkIndex,
      initialized: false
    };
  }

  const now = new Date().toISOString();
  const state = {
    syncId,
    createdAt: now,
    updatedAt: now,
    status: "in_progress",
    nextChunkIndex: 0,
    totalChunks: request.chunks.length,
    totalConsultants: request.syncPlan.totalConsultants || request.request?.emails?.length || 0,
    completedChunks: [],
    failedChunks: [],
    outputFile: request.output?.directConnectorEventsFile || join(dirname(requestFile), "direct-connector-events.js"),
    requestFile,
    lastMerge: null
  };
  writeJson(stateFile, state);

  return {
    stateFile,
    syncId,
    status: state.status,
    nextChunkIndex: state.nextChunkIndex,
    initialized: true
  };
}

function markFailed({ stateFile, chunkIndex, message }) {
  const state = readJson(stateFile);
  const failed = new Set(state.failedChunks || []);
  failed.add(chunkIndex);
  state.status = "in_progress";
  state.updatedAt = new Date().toISOString();
  state.failedChunks = [...failed].sort((a, b) => a - b);
  state.lastError = { chunkIndex, message, at: state.updatedAt };
  writeJson(stateFile, state);
  return state;
}

function parseToolText(result) {
  const content = result?.Ok?.content || result?.content || [];
  const textItem = content.find(item => item && item.type === "text" && item.text);
  if (!textItem) return null;
  return JSON.parse(textItem.text);
}

function extractToolResults({ requestFile, rolloutFile, chunkIndex, resultFile }) {
  const request = readJson(requestFile);
  const chunk = request.chunks[chunkIndex];
  if (!chunk) throw new Error(`Chunk ${chunkIndex} was not found in ${requestFile}`);

  const expectedEmails = new Set(chunk.emails.map(normalizeEmail));
  const requestedStart = `${request.request.start}T00:00:00`;
  const requestedEnd = `${request.request.end}T00:00:00`;
  const requestedTimeZone = request.request.timeZone || "UTC";
  const requestedInterval = Number(request.request.intervalMinutes || 30);
  const byEmail = new Map();
  const errors = [];

  const lines = readFileSync(rolloutFile, "utf8").split(/\r?\n/).filter(Boolean);
  lines.forEach(line => {
    let row;
    try {
      row = JSON.parse(line);
    } catch {
      return;
    }

    const payload = row.payload;
    if (payload?.type !== "mcp_tool_call_end") return;
    if (payload.invocation?.server !== "codex_apps") return;
    if (payload.invocation?.tool !== "microsoft outlook calendar_get_schedule") return;

    const args = payload.invocation.arguments || {};
    if (args.start?.dateTime !== requestedStart) return;
    if (args.end?.dateTime !== requestedEnd) return;
    if ((args.start?.timeZone || "UTC") !== requestedTimeZone) return;
    if (Number(args.availability_view_interval || 30) !== requestedInterval) return;

    const schedules = Array.isArray(args.schedules) ? args.schedules.map(normalizeEmail) : [];
    if (!schedules.some(email => expectedEmails.has(email))) return;

    let data;
    try {
      data = parseToolText(payload.result);
    } catch (error) {
      errors.push({ message: `Could not parse tool result: ${error.message}`, schedules });
      return;
    }

    (data?.value || []).forEach(schedule => {
      const email = normalizeEmail(schedule.schedule_id || schedule.scheduleId);
      if (!expectedEmails.has(email)) return;
      if (schedule.error) {
        errors.push({ email, message: schedule.error.message || String(schedule.error) });
      }
      if (schedule.availability_view || schedule.availabilityView) {
        byEmail.set(email, {
          email,
          start: `${request.request.start}T00:00:00.000Z`,
          intervalMinutes: requestedInterval,
          view: String(schedule.availability_view || schedule.availabilityView)
        });
      }
    });
  });

  const loadedEmails = [...byEmail.keys()].sort();
  const missingEmails = [...expectedEmails].filter(email => !byEmail.has(email)).sort();
  const result = {
    refreshedAt: new Date().toISOString(),
    start: request.request.start,
    end: request.request.end,
    intervalMinutes: requestedInterval,
    roster: chunk.roster || [],
    loadedEmails,
    availability: loadedEmails.map(email => byEmail.get(email)),
    events: [],
    errors: [
      ...errors,
      ...missingEmails.map(email => ({ email, message: "No matching getSchedule result found in rollout log." }))
    ]
  };

  writeJson(resultFile, result);
  return {
    resultFile,
    chunkIndex,
    loadedEmails: loadedEmails.length,
    missingEmails: missingEmails.length,
    errors: result.errors
  };
}

function printUsage() {
  console.log([
    "Usage:",
    "  node calendar-codex-sync-tools.mjs init-state [requestFile] [stateFile] [--force]",
    "  node calendar-codex-sync-tools.mjs status [requestFile] [stateFile]",
    "  node calendar-codex-sync-tools.mjs merge-result <chunkIndex> <resultFile> [requestFile] [stateFile]",
    "  node calendar-codex-sync-tools.mjs mark-failed <chunkIndex> <message> [stateFile]",
    "  node calendar-codex-sync-tools.mjs extract-tool-results <chunkIndex> <rolloutFile> <resultFile> [requestFile]"
  ].join("\n"));
}

const [command, ...args] = process.argv.slice(2);

try {
  if (command === "status") {
    console.log(JSON.stringify(showStatus({
      requestFile: args[0] || DEFAULT_REQUEST_FILE,
      stateFile: args[1] || DEFAULT_STATE_FILE
    }), null, 2));
  } else if (command === "init-state") {
    console.log(JSON.stringify(initState({
      requestFile: args[0] || DEFAULT_REQUEST_FILE,
      stateFile: args[1] || DEFAULT_STATE_FILE,
      force: args.includes("--force")
    }), null, 2));
  } else if (command === "merge-result") {
    const chunkIndex = Number(args[0]);
    const resultFile = args[1];
    if (!Number.isInteger(chunkIndex) || !resultFile) throw new Error("merge-result requires <chunkIndex> and <resultFile>.");
    console.log(JSON.stringify(mergeResult({
      chunkIndex,
      resultFile,
      requestFile: args[2] || DEFAULT_REQUEST_FILE,
      stateFile: args[3] || DEFAULT_STATE_FILE
    }), null, 2));
  } else if (command === "mark-failed") {
    const chunkIndex = Number(args[0]);
    const message = args[1] || "Connector call failed.";
    if (!Number.isInteger(chunkIndex)) throw new Error("mark-failed requires <chunkIndex>.");
    console.log(JSON.stringify(markFailed({
      chunkIndex,
      message,
      stateFile: args[2] || DEFAULT_STATE_FILE
    }), null, 2));
  } else if (command === "extract-tool-results") {
    const chunkIndex = Number(args[0]);
    const rolloutFile = args[1];
    const resultFile = args[2];
    if (!Number.isInteger(chunkIndex) || !rolloutFile || !resultFile) {
      throw new Error("extract-tool-results requires <chunkIndex>, <rolloutFile>, and <resultFile>.");
    }
    console.log(JSON.stringify(extractToolResults({
      chunkIndex,
      rolloutFile,
      resultFile,
      requestFile: args[3] || DEFAULT_REQUEST_FILE
    }), null, 2));
  } else {
    printUsage();
    process.exitCode = command ? 1 : 0;
  }
} catch (error) {
  console.error(error.message);
  process.exitCode = 1;
}
